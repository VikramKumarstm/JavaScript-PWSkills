// abs
// abs(positive_value) -> positive_value
// abs(negative_value) -> positive_counterpart_of that negative value
// |x| -> Ex: |10| -> 10, |-8| -> 8 

console.log(Math.abs(-9));
console.log(Math.abs(8));

let a = 's';
let b = Number(a);
console.log(Math.abs(b));

// Round off ->

console.log(Math.round(14.6));

